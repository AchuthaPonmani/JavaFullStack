package Inheritance;

public class Person {


        private String name;
        private int age;

        // Constructor for Person class
        public Person(String name, int age) {
            this.name = name;
            this.age = age;
        }

        // Getter methods for name and age
        public String getName() {
            return name;
        }

        public int getAge() {
            return age;
        }
    }



